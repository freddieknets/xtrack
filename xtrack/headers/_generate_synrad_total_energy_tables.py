"""
Generate deterministic inverse-CDF tables for total synchrotron-radiation
energy loss.

The runtime ``quantum-efficient`` radiation mode does not need individual
photon energies. It only needs the total energy lost in a slice. For a fixed
number of emitted photons N, this script tabulates the distribution of

    X_N = x_1 + x_2 + ... + x_N,

where each ``x_i`` is an independent photon energy normalized by the critical
energy, ``x_i = E_i / E_c``. Tables are generated for powers of two,
``N = 1, 2, 4, ..., 256``.

The table generation is deterministic, not Monte Carlo:

1. The single-photon synchrotron spectrum ``S(x)`` is integrated to build a
   high-accuracy one-photon CDF. Near zero the integration uses ``x = y^3`` to
   remove the integrable ``x^(-2/3)`` singularity.
2. Multi-photon distributions are produced by repeated self-convolution in
   quantile/CDF space:
   CDF_2 = CDF_1 * CDF_1, CDF_4 = CDF_2 * CDF_2, and so on.
3. Each resulting CDF is inverted on a probability grid concentrated in both
   tails, currently down to ``TAIL_PROBABILITY_MIN``.
4. The generated C header stores ``log(X_N)`` quantiles, which improves
   interpolation in the low-energy tail where values span many orders of
   magnitude.

At runtime, after sampling the photon count from a Poisson distribution, the
count is decomposed into powers of two. For example, 312 photons are sampled as
``256 + 32 + 16 + 8``. One inverse-CDF table lookup is done for each chunk, and
the sampled chunk energies are summed. This replaces hundreds of individual
photon-energy samples with a few table lookups while preserving the total
energy-loss distribution up to the accuracy of the deterministic tables and
interpolation.
"""

from pathlib import Path

import numpy as np
from scipy.integrate import cumulative_trapezoid


POWERS = [1 << ii for ii in range(9)]

TAIL_PROBABILITY_MIN = 1e-15
TAIL_PROBABILITY_MAX = 1e-2
TAIL_POINTS_PER_DECADE = 160

# Offline generation tolerances. These affect generation/certification only,
# not the runtime table layout. TAIL_PROBABILITY_MIN already resolves the
# runtime inverse-CDF grid down to 1e-15; tightening it further should be driven
# by validation of tail observables, because it increases generated table size
# and therefore runtime cache pressure.
GEN_REL_TOL = 1e-12
GEN_ABS_TOL = 1e-15
TABLE_LOG_TOL = 1e-9

# All runtime tables are built with the same high-accuracy generation route.
# This avoids a handoff between methods and prevents small numerical drifts
# between low-count and high-count tables. Near zero, F_N(S) scales
# approximately as S^(N/3), so the higher-count tables are intrinsically
# smoother, but they are still generated by repeated certified convolution for
# consistency.
LOW_COUNT_SEED_POWERS = tuple(POWERS)
REFERENCE_TAIL_POINTS_PER_DECADE = 260
REFERENCE_CENTER_POINTS = 5001
CONVOLUTION_QUAD_TAIL_POINTS_PER_DECADE = 220
CONVOLUTION_QUAD_CENTER_POINTS = 4001
BINARY_SEARCH_ITERATIONS = 90

# Uniform grid used for convolution of normalized photon energies.
DX = 1e-4
X_MAX = 256.0

# Dense auxiliary grid near x=0 resolves the x^(-2/3) integrable singularity
# before the CDF is interpolated onto the uniform convolution grid.
N_LOW_GRID = 120_000
X_LOW_MAX = 1e-2



def make_probability_grid(tail_probability_min=TAIL_PROBABILITY_MIN,
                          tail_probability_max=TAIL_PROBABILITY_MAX,
                          tail_points_per_decade=TAIL_POINTS_PER_DECADE,
                          center_points=CENTER_POINTS):
    """Build the inverse-CDF probability grid, with extra points in both tails."""
    n_tail = int(round(
        np.log10(tail_probability_max / tail_probability_min)
        * tail_points_per_decade))
    u_tail = np.logspace(
        np.log10(tail_probability_min),
        np.log10(tail_probability_max),
        n_tail,
    )
    u_center = np.linspace(
        tail_probability_max, 1 - tail_probability_max, center_points)
    return np.unique(np.concatenate((
        [0.0], u_tail, u_center, 1.0 - u_tail[::-1], [1.0])))


def make_segmented_probability_grid(
        tail_probability_min=TAIL_PROBABILITY_MIN,
        tail_probability_max=TAIL_PROBABILITY_MAX,
        tail_points_per_decade=TAIL_POINTS_PER_DECADE,
        center_points=CENTER_POINTS):
    """Build the runtime probability grid in numerically stable segments.

    The returned table order is:

    - left tail in ``u``;
    - center in ``u``;
    - right tail in survival probability ``v = 1 - u``.

    The right tail is stored in ``v`` to avoid collapse of dense ``1 - v``
    points near one in double precision.
    """
    n_tail = int(round(
        np.log10(tail_probability_max / tail_probability_min)
        * tail_points_per_decade))
    u_tail = np.logspace(
        np.log10(tail_probability_min),
        np.log10(tail_probability_max),
        n_tail,
    )
    u_center = np.linspace(
        tail_probability_max, 1 - tail_probability_max, center_points)

    # Keep the tail boundary in the tail segments as well as in the center.
    # The duplicated boundary values live in separate segments, so they do not
    # make any individual segment non-monotonic. They are needed to avoid a
    # lookup gap between the last logarithmic tail point and the first center
    # point.
    left_u = np.concatenate(([0.0], u_tail))
    right_v = np.concatenate(([0.0], u_tail))

    probabilities_in_table_order = np.concatenate((
        left_u,
        u_center,
        1.0 - right_v,
    ))

    return {
        "left_u": left_u,
        "center_u": u_center,
        "right_v": right_v,
        "probabilities": probabilities_in_table_order,
    }


def probability_grid_duplicate_diagnostics(
        tail_probability_min=TAIL_PROBABILITY_MIN,
        tail_probability_max=TAIL_PROBABILITY_MAX,
        tail_points_per_decade=TAIL_POINTS_PER_DECADE,
        center_points=CENTER_POINTS):
    """Return duplicate diagnostics for the emitted probability grid.

    The high-probability tail is represented as ``1 - u_tail``. Near one,
    several requested points can collapse to the same double-precision value.
    That is relevant for high-energy tail validation, not only for lookup
    indexing.
    """
    n_tail = int(round(
        np.log10(tail_probability_max / tail_probability_min)
        * tail_points_per_decade))
    u_tail = np.logspace(
        np.log10(tail_probability_min),
        np.log10(tail_probability_max),
        n_tail,
    )
    u_center = np.linspace(
        tail_probability_max, 1 - tail_probability_max, center_points)
    requested = np.concatenate((
        [0.0], u_tail, u_center, 1.0 - u_tail[::-1], [1.0]))
    emitted = np.unique(requested)
    right_requested = 1.0 - u_tail[::-1]
    right_unique = np.unique(right_requested)
    return {
        "requested_size": requested.size,
        "emitted_size": emitted.size,
        "n_duplicates": requested.size - emitted.size,
        "right_requested_size": right_requested.size,
        "right_unique_size": right_unique.size,
        "right_duplicates": right_requested.size - right_unique.size,
    }


